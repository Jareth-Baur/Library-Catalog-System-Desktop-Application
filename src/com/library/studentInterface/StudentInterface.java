    private void recordToBrowsingHistory(String bookID, String title, String author, String genre, String publicationDate, String status) {
        LocalDateTime time = LocalDateTime.now();
        String hour = time.getHour() + "", minute = time.getMinute() + "";
        Book perBook = new Book(Integer.parseInt(bookID), title, author, genre, publicationDate, status, (hour + ":" + minute));
        if (!(browsedBooks.contains(perBook))) {
            browsedBooks.add(perBook);
        }
    }

    private void populateBrowsingHistoryTable(String sortBy) {
        switch (sortBy) {
            case "Book ID" ->
                Collections.sort(browsedBooks, Book.bookIDComparator);
            case "Title" ->
                Collections.sort(browsedBooks, Book.titleComparator);
            case "Author" ->
                Collections.sort(browsedBooks, Book.authorComparator);
            case "Genre" ->
                Collections.sort(browsedBooks, Book.genreComparator);
            case "Publication Date" ->
                Collections.sort(browsedBooks, Book.publicationDateComparator);
            case "Browsed Time" ->
                Collections.sort(browsedBooks, Book.timeComparator);
            default ->
                throw new AssertionError();
        }
        DefaultTableModel model = (DefaultTableModel) browsingHistoryResultTable.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (Book perBook : browsedBooks) {
            Object[] row = {
                perBook.getBookID(),
                perBook.getTitle(),
                perBook.getAuthor(),
                perBook.getGenre(),
                perBook.getPublicationDate(),
                perBook.getStatus(),
                perBook.getTime()
            };
            model.addRow(row);
        }
    }


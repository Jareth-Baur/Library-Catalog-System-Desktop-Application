    private void displayDateOnComboBox() {
        LocalDateTime date = LocalDateTime.now();
        int month, year;
        int day;
        month = date.getMonthValue();
        day = date.getDayOfMonth();
        year = date.getYear();
        for (int i = 0; i < 7; i++) {
            returnDate.addItem(year + "-" + month + "-" + day);
            day++;
        }
    }

    private void addBookLoan(String studentID, String bookID) {
        LocalDate borrowDate = LocalDate.now();
        try {
            String insertQuery = "INSERT INTO `borrows`(studentID, bookID, borrowDate, dueDate) VALUES (?,?,?,?)";
            Connection connection = DriverManager.getConnection(SQLURL, SQLUSERNAME, SQLPASSWORD);
            PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
            preparedStatement.setString(1, studentID);
            preparedStatement.setString(2, bookID);
            preparedStatement.setString(3, borrowDate.toString());
            preparedStatement.setString(4, returnDate.getSelectedItem().toString());
            int rowsAffected = preparedStatement.executeUpdate();
            if (rowsAffected > 0) {
                System.out.println("Loaned A Book Successfully.");
                JOptionPane.showMessageDialog(null, "Book loaned successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                updateBookStatus(bookID);
                this.dispose();
            }
            connection.close();
            preparedStatement.close();
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

    private void updateBookStatus(String bookID) {
        try {
            String insertQuery = "UPDATE books SET status = 'unavailable' WHERE bookID = ?";
            Connection connection = DriverManager.getConnection(SQLURL, SQLUSERNAME, SQLPASSWORD);
            PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
            preparedStatement.setString(1, bookID);
            int rowsAffected = preparedStatement.executeUpdate();
            if (rowsAffected > 0) {
                System.out.println("Changes book status Successfully." + " Rows Affected: " + rowsAffected);
            }
            connection.close();
            preparedStatement.close();
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

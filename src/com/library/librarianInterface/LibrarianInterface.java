    // Displayes the user information to the profile section
    private void displayProfileInformationDetails() {
        try (Connection connection = DriverManager.getConnection(sqlUrl, sqlUsername, sqlPassword); PreparedStatement preparedStatement = connection.prepareStatement("SELECT fullName, username, email, phoneNumber, jobTitle FROM librarians WHERE userName = ?")) {
            preparedStatement.setString(1, username);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    fullNameLabel.setText("Full Name: " + resultSet.getString("fullName"));
                    usernameLabel.setText("Username: " + resultSet.getString("username"));
                    emailLabel.setText("Email: " + resultSet.getString("email"));
                    phoneNumberLabel.setText("Phone Number: " + resultSet.getString("phoneNumber"));
                    jobTitleLabel.setText("Job Title: " + resultSet.getString("jobTitle"));
                }
            }
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }


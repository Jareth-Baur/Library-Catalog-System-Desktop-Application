    public void getMostUsedBook() {
        try {
            // Query to count occurrences of each bookID
            String countQuery = "SELECT bookID, COUNT(*) AS borrowCount FROM borrows GROUP BY bookID ORDER BY borrowCount DESC LIMIT 1";
            Connection connection = DriverManager.getConnection(sqlUrl, sqlUsername, sqlPassword);
            PreparedStatement countStatement = connection.prepareStatement(countQuery);
            ResultSet countResultSet = countStatement.executeQuery();

            // Retrieve the most used bookID and its count
            int mostUsedBookID = -1;
            int maxCount = 0;
            if (countResultSet.next()) {
                mostUsedBookID = countResultSet.getInt("bookID");
                maxCount = countResultSet.getInt("borrowCount");
            }

            // Query to fetch details of the most used book from the books table
            if (mostUsedBookID != -1) {
                String bookQuery = "SELECT * FROM books WHERE bookID = ?";
                PreparedStatement bookStatement = connection.prepareStatement(bookQuery);
                bookStatement.setInt(1, mostUsedBookID);
                ResultSet bookResultSet = bookStatement.executeQuery();

                if (bookResultSet.next()) {
                    String title = bookResultSet.getString("title");
                    String author = bookResultSet.getString("author");
                    String genre = bookResultSet.getString("genre");
                    // Set the Book details to corresponding labels
                    mosedUsedBookTitleLabel.setText("Book Title: " + title);
                    mostUsedBookAuthorLabel.setText("Author: " + author);
                    mosedUsedBookGenreLabel.setText("Genre: " + genre);
                }
            } else {
                // Set label text to indicate no most used book
                mosedUsedBookTitleLabel.setText("No Most Used Book");
                mostUsedBookAuthorLabel.setText("");
                mosedUsedBookGenreLabel.setText("");
            }
        } catch (SQLException e) {
            System.out.println("Database error: " + e.getMessage());
        }
    }
